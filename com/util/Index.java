package com.util;

public class Index {

	int rfcNo;
	String rfcTitle;
	Peer peer;

	public int getRfcNo() {
		return rfcNo;
	}

	public void setRfcNo(int rfcNo) {
		this.rfcNo = rfcNo;
	}

	public String getRfcTitle() {
		return rfcTitle;
	}

	public void setRfcTitle(String rfcTitle) {
		this.rfcTitle = rfcTitle;
	}

	public Peer getPeer() {
		return peer;
	}

	public void setPeer(Peer peer) {
		this.peer = peer;
	}

}
